# A Set is a collection which is unordered an unindexed. No duplicate members.

# The set use to be inside in curly brackets or braces.

# Create set

fruits_set = {"Apples", "Orange", "Mango"}


# check if in set

print("Apples" in fruits_set)

# Add to set
fruits_set.add("Grape")

# Remove frome set
fruits_set.remove("Apples")
print(fruits_set)